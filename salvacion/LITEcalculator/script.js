// The goal for this exercise is to create a calculator that does the following:
// add, subtract, get the sum, multiply, get the power, and find the factorial





let sumar = (n1, n2) => {
  return n1 + n2;
}

let restar = (n1, n2) => {
  return n1 - n2;
}

let multiplicar = (n1, n2) => {
  return n1 * n2;
}

let dividir = (n1, n2) => {
  return n1/n2;
}





